function setLang(lang){
  try{
    localStorage.setItem('sp_lang', lang);
  }catch(e){}
  const map = dict[lang] || dict['ja'];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if(map[key]) el.textContent = map[key];
  });
}

